package ptt.vn.icaremobileapp.chipview;

public class ChipObject {
    private int sort;
    private int chon;
    private int id;
    private String code;
    private String name;
    private String unit;
    private String diengiai;
    private int qty;
    private int price;
    private int giabhyt;
    private int phuthu;
    private int bhyt;
    private int medexa;

    public ChipObject() {
    }

    public int getSort() {
        return sort;
    }

    public void setSort(int sort) {
        this.sort = sort;
    }

    public int getChon() {
        return chon;
    }

    public void setChon(int chon) {
        this.chon = chon;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

    public String getDiengiai() {
        return diengiai;
    }

    public void setDiengiai(String diengiai) {
        this.diengiai = diengiai;
    }

    public int getQty() {
        return qty;
    }

    public void setQty(int qty) {
        this.qty = qty;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getGiabhyt() {
        return giabhyt;
    }

    public void setGiabhyt(int giabhyt) {
        this.giabhyt = giabhyt;
    }

    public int getPhuthu() {
        return phuthu;
    }

    public void setPhuthu(int phuthu) {
        this.phuthu = phuthu;
    }

    public int getBhyt() {
        return bhyt;
    }

    public void setBhyt(int bhyt) {
        this.bhyt = bhyt;
    }

    public int getMedexa() {
        return medexa;
    }

    public void setMedexa(int medexa) {
        this.medexa = medexa;
    }
}

