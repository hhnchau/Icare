package ptt.vn.icaremobileapp.model.patient;

public class PatientIde {
    private String idline;
    private String cardid;
    private String daterg;
    private String place;
    private String imgpth;
    private int status;
    private String image;


    public PatientIde() {
    }


    public String getIdline() {
        return idline;
    }

    public void setIdline(String idline) {
        this.idline = idline;
    }

    public String getCardid() {
        return cardid;
    }

    public void setCardid(String cardid) {
        this.cardid = cardid;
    }

    public String getDaterg() {
        return daterg;
    }

    public void setDaterg(String daterg) {
        this.daterg = daterg;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public String getImgpth() {
        return imgpth;
    }

    public void setImgpth(String imgpth) {
        this.imgpth = imgpth;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
